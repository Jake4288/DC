            sprite.Position = new Vector2(300, 300);

            Character character = new Character(sprite);
            Character enemcharacter = new Character(enemsprite);
                        
            player = new Player(this, character);
            enemy = new Enemy(this, enemcharacter, player);

            player.LoadContent(this.Content);
            enemy.LoadContent(this.Content);

